
colors1 = ("red","blue","green","yellow","pink","black")
print(colors1)

# ('red', 'blue', 'green')
colors2 = colors1[0:3]
print(colors2)

# ('green', 'yellow')
colors3 = colors1[2:4]
print(colors3)

# ('yellow', 'pink', 'black')
colors4 = colors1[3:]
print(colors4)

# ('red', 'green', 'pink')
colors5 = colors1[::2]
print(colors5)

# ('black', 'pink', 'yellow', 'green', 'blue', 'red')
colors6 = colors1[::-1]
print(colors6)
